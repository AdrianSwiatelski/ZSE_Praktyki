package second_week.loops.zadania_spotkanie5;

public class Zad_petle_2 {
    public static void main(String[] args) {
        System.out.println("Kolejność malejąca co 3 od 30: ");
        for (int number = 30; number >= 0; number-=3) {
            System.out.println(number + "\t");
        }
    }
}
